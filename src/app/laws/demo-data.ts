/**
 * الهرم القضائي السعودي الفعلي — مصادر المبادئ
 *
 * ─── القضاء العادي (تحت إشراف المجلس الأعلى للقضاء) ────────────────────────
 *
 *   supreme          → المحكمة العليا (ك ع) — القمة الحالية منذ 1428هـ
 *   supreme-general  → المحكمة العليا بهيئتها العامة (ك ع ع)
 *   permanent-cmte   → مجلس القضاء الأعلى بهيئته الدائمة (م ق د)
 *                      ← المصدر الرئيسي لمجموعات المبادئ القضائية المُرقَّمة
 *   sjc-general      → مجلس القضاء الأعلى بهيئته العامة (م ق ع)
 *   higher-judicial  → الهيئة القضائية العليا (هـ ق ع) — تاريخية، قبل 1372هـ
 *   tamyeez          → محكمة التمييز — تاريخية، قبل تحولها للمحكمة العليا 1428هـ
 *                      ← تستخدم تنسيق "التقرير"، وليس "المبدأ"
 *   appeal-courts    → محاكم الاستئناف
 *   specialized      → المحكمة الجزائية المتخصصة
 *   sjc              → المجلس الأعلى للقضاء — تعاميم إدارية قضائية
 *
 * ─── ديوان المظالم (القضاء الإداري — مستقل) ────────────────────────────────
 *
 *   admin-supreme    → المحكمة الإدارية العليا
 *   admin-appeal     → محاكم الاستئناف الإدارية
 *   admin-first      → المحاكم الإدارية الابتدائية
 *
 * ─── شبه قضائية ──────────────────────────────────────────────────────────────
 *
 *   sama-cmte        → الأمانة العامة للجان المنازعات والمخالفات المصرفية والتمويلية (ساما)
 *   cma-cmte         → هيئة السوق المالية — لجان الفصل
 *   zatca-cmte       → اللجنة الاستئنافية للمخالفات والمنازعات الضريبية (الزكاة والجمارك)
 *   commercial-paper → اللجنة القانونية الاستئنافية لقرارات مكاتب الفصل في منازعات الأوراق التجارية
 *   labor-board      → الهيئة العليا لتسوية الخلافات العمالية (تاريخية — قبل نقل الاختصاص)
 *   prosecution      → النيابة العامة (توجيهات ومبادئ)
 */
