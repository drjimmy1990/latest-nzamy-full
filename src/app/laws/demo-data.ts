export interface DemoPrinciple {
  id: string;
  // ─── هوية المصدر ─────────────────────────────────────────────────────────
  source: string;               // اسم الجهة للعرض المقروء
  sourceId: PrincipleSourceId;  // معرف الجهة للفلترة
  srcAbbr?: PrincipleSourceAbbr; // الاختصار الرسمي (م ق د، ك ع، إلخ)
  // ─── هوية المبدأ ─────────────────────────────────────────────────────────
  principleNum?: string;        // رقم المبدأ، مثل "(483)" أو "483"
  reportNum?: string;           // رقم التقرير (لمحكمة التمييز فقط)، مثل "786 / 13"
  caseNum?: string;             // رقم القرار / الدعوى، مثل "1829/ش/ب"
  ref: string;                  // نص المرجع الكامل للعرض
  page?: string;                // رقم الصفحة في المجموعة
  // ─── المحتوى ────────────────────────────────────────────────────────────
  text: string;                 // نص المبدأ حرفياً
  subject: PrincipleSubject;
  cat: string;                  // SA-XX
  year: string;                 // السنة الهجرية
}

